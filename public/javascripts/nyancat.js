var Particle = function(spec, my) {
  var that = {}
    , my = my || {}
    , _canvas = my.canvas
    , _context = _canvas.getContext('2d')
    , k = spec.k
    , margin = 40
    , x = _canvas.width * Math.random()
    , y = _canvas.height * Math.random()
    , dot = 4
    , diff = 100
    ;

  that = {
    draw: function () {
      _context.fillStyle = 'rgb(255,255,255)';
      for (var i = 0; i < particle_data[0].length; i++) {
        for (var j = 0; j < particle_data[0][0].length; j++) {
          if (particle_data[k][i][j] == 1) {
            _context.fillRect(x + i * dot - k * margin, y + j * dot, dot, dot);
          }
        }
      }
      k++;
      if (k > particle_data.length - 1) {
        k = 0;
        x = (_canvas.width + diff) * Math.random() + diff;
        y = _canvas.height * Math.random();
      }
    },
    clear: function () {
      _context.clearRect(0, 0, _canvas.width, _canvas.height);
    }
  };

  return that;
}

var Rainbow = function(spec, my) {
  var that = {}
    , my = my || {}
    , _canvas = my.canvas
    , _context = _canvas.getContext('2d')
    , sign = -1
    , k = 0
    , x0 = spec.x
    , y0 = spec.y
    ;

  that = {
    draw: function () {
      if (k % 3 == 0) {
        sign = -sign;
      }
      for(var i = 0, x = x0, y = y0 - 2 * sign * spec.weight / 2
          ; i < spec.number; i++) {
        if (i % 4 == 0 && i > 3) {
          sign = -sign;
        }
        y = y + sign * spec.weight / 2;
        var count = 0;
        for (var _color in spec.color) {
          _context.fillStyle = 'rgb(' + spec.color[_color] + ')';
          _context.fillRect(x, y, spec.width, spec.weight);
          y += spec.weight;
          count++;
        }
        y -= spec.weight * count;
        x += spec.width;
      }
      k++;
    },
    clear: function () {
      _context.clearRect(0, 0, _canvas.width, _canvas.height);
    }
  };

  return that;
}

