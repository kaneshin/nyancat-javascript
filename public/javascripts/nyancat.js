var Nyan = function(spec, my) {
  var that = {}
    , my = my || {}
    , _canvas = my.canvas
    , _context = _canvas.getContext('2d')
    , dot = 6
    , k = 0
    , x = _canvas.width / 2 - 5
    , y = _canvas.height / 2 - 60
    ;

  that = {
    draw: function () {
      for (var i = 0; i < nyan_data[0].length; i++) {
        for (var j = 0; j < nyan_data[0][0].length; j++) {
          switch (nyan_data[k][i][j]) {
            case 1: // #000000
              _context.fillStyle = 'rgb(0,0,0)';
              _context.fillRect(x + j * dot, y + i * dot, dot, dot);
              break;
            case 2: // #999999
              _context.fillStyle = 'rgb(153,153,153)';
              _context.fillRect(x + j * dot, y + i * dot, dot, dot);
              break;
            case 3: // #ff99cc
              _context.fillStyle = 'rgb(255,153,204)';
              _context.fillRect(x + j * dot, y + i * dot, dot, dot);
              break;
            case 4: // #ffcc99
              _context.fillStyle = 'rgb(255,204,153)';
              _context.fillRect(x + j * dot, y + i * dot, dot, dot);
              break;
            case 5: // #ffffff
              _context.fillStyle = 'rgb(255,255,255)';
              _context.fillRect(x + j * dot, y + i * dot, dot, dot);
              break;
            case 6: // #ff6699
              _context.fillStyle = 'rgb(255,102,153)';
              _context.fillRect(x + j * dot, y + i * dot, dot, dot);
              break;
            case 7: // #ff3399
              _context.fillStyle = 'rgb(255,51,153)';
              _context.fillRect(x + j * dot, y + i * dot, dot, dot);
              break;
            case 0:
              break;
          }
        }
      }
      k++;
      if (k > nyan_data.length - 1) {
        k = 0;
      }
    },
    clear: function () {
      _context.clearRect(0, 0, _canvas.width, _canvas.height);
    }
  };

  return that;
}

